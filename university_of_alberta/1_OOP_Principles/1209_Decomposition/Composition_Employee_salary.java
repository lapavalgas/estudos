
/**
 * Is a class java representation of a composition. An type of relationship
 * between classes.
 */

public class Composition_Employee_salary {
    /**
     * This demonstrated that the Employee 'has-a' salary..
     */
    private float salary;

    /**
     * This constructor demonstrated that the Employee salary is set-ed inside and together this class.
     */
    public Employee(float salary){
        this.salary = salary;
    }

}